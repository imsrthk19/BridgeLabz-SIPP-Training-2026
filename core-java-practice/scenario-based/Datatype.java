public class Datatype{
    public static void main(String[] args){
        int a = 5000;
        short b = 1200;
        long c = 19786237L;
        float d = 34.6f;
        char e = 'D';
        boolean f = true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}